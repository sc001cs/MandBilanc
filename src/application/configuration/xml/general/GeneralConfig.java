package application.configuration.xml.general;

public class GeneralConfig {

	private String data_format_file;
	private String utf8;
	private String path_folder;
	private String path_folder_excel;
	
	public String getData_format_file() {
		return data_format_file;
	}
	public void setData_format_file(String data_format_file) {
		this.data_format_file = data_format_file;
	}
	public String getUtf8() {
		return utf8;
	}
	public void setUtf8(String utf8) {
		this.utf8 = utf8;
	}
	public String getPath_folder() {
		return path_folder;
	}
	public void setPath_folder(String path_folder) {
		this.path_folder = path_folder;
	}
	public String getPath_folder_excel() {
		return path_folder_excel;
	}
	public void setPath_folder_excel(String path_folder_excel) {
		this.path_folder_excel = path_folder_excel;
	}
}
